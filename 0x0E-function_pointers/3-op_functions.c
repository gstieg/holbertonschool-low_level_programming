#include "3-calc.h"

/**
 * op_add - adds
 *
 * @a: integer
 *
 * @b: integer
 *
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts
 *
 * @a: integer
 *
 * @b: integer
 *
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies
 *
 * @a: integer
 *
 * @b: integer
 *
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides
 *
 * @a: integer
 *
 * @b: integer
 *
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modular
 *
 * @a: integer
 *
 * @b: integer
 *
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
